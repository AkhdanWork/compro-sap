<script>
document.addEventListener('DOMContentLoaded', function() {
    const teamCards = document.querySelectorAll('.team-card');
    
    teamCards.forEach(card => {
        const cardInner = card.querySelector('.card-inner');
        const cardName = card.querySelector('.card-name');
        let isFlipped = false;
        
        // Handle click/tap untuk mobile
        card.addEventListener('click', function(e) {
            // Jangan flip jika klik pada link sosial media
            if (e.target.closest('a')) return;
            
            isFlipped = !isFlipped;
            
            if (isFlipped) {
                cardInner.style.transform = 'rotateY(180deg)';
                if (cardName) {
                    cardName.style.opacity = '0';
                    cardName.style.pointerEvents = 'none';
                }
            } else {
                cardInner.style.transform = 'rotateY(0deg)';
                if (cardName) {
                    cardName.style.opacity = '1';
                    cardName.style.pointerEvents = 'auto';
                }
            }
        });
        
        // Handle hover untuk desktop
        if (window.innerWidth >= 1024) {
            card.addEventListener('mouseenter', function() {
                cardInner.style.transform = 'rotateY(180deg)';
                if (cardName) {
                    cardName.style.opacity = '0';
                    cardName.style.pointerEvents = 'none';
                }
            });
            
            card.addEventListener('mouseleave', function() {
                cardInner.style.transform = 'rotateY(0deg)';
                if (cardName) {
                    cardName.style.opacity = '1';
                    cardName.style.pointerEvents = 'auto';
                }
            });
        }
    });
});
</script>

